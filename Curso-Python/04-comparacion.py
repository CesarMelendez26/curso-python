n1 = 10
n2 = 15
#se pueden comparar dos valores con mayor que (>) y nos devuelve falso o verdadero.
print(n1 > n2)
#se pueden comprar dos valores con menor que (<) y nos devuelve falso o verdadero.
print(n1 < n2)
#se pueden comprar dos valores con menor o igual que (<=) y nos devuelve falso o verdadero.
print(n1 <= 10)
#se pueden comprar dos valores con mayor o igual que (>=) y nos devuelve falso o verdadero.
print(n1 >= 10)
#se pueden comprar dos valores exactamente igual que (==) y nos devuelve falso o verdadero.
print(n1 == 20)