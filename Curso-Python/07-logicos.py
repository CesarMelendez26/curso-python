edad = 22
#los operadores se operan de izquierda a derecha y solo hace falta que condicion sea falsa para que arroje FALSE
print(edad > 18 and edad < 30)
#el operador OR se usa para que el programa evalue si alguna de las dos es cierta
print(edad > 18 or edad < 30)
#niega la expresion que se encuentre a la derecha cambiando de falso a verdadero y viceversa.
print(not (edad > 17))
